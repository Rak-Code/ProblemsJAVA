public class Power {
    public static void main(String[] args) {
        int base = 3;  
        int exponent = 2;  
        int result = 1;  
        for (int i = 0; i < exponent; i++) {  
            result *= base;  
        }  
        System.out.println(base + " raised to the power of " + exponent + " is " + result);  

        
    
    
    }
    
}
